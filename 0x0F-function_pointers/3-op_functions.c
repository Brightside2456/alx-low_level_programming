#include "3-cal.h"
/**
 * op_add - add num1 and num2
 * @a: num1
 * @b: num2
 * Return: computed value
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mul - multiply num1 and num 2
 * @a: num1
 * @b: num2
 * Return: computed value
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide num1 by num 2
 * @a: num1
 * @b: num2
 * Return: computed value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_modulo - find the remainder of a divi btn num1 and num2
 * @a: num1
 * @b: num2
 * Return: computed value
 */
int op_modulo(int a, int b)
{
	return (a % b);
}
/**
 * op_sub - subtract num2 from num1
 * @a: num1
 * @b: num2
 * Return: computed value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
